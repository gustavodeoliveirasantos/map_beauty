class Findation {
  static void loadData() {
    // loadBrandIds();q
    //loadProductOfBrandId(1);
    loadShadesByProductId();
  }

  static void loadBrandIds() async {
    var jsonData = await rootBundle.loadString('assets/brands.json');

    var brandList = List<BrandModel>.from((jsonDecode(jsonData) as List).map((e) => BrandModel.fromMap(e)));

    for (final brand in brandList) {
      //  print("${brand.id};${brand.name};${brand.search}");
      await loadProductOfBrandId(brand.id, brand.name);
    }
  }

  static Future<void> loadProductOfBrandId(int id, String brandName) async {
    var url = "https://findation.com/products?brand_id=";
    var completeUrl = "$url$id/";

    final response = await http.get(Uri.parse(completeUrl));

    final jsonData = response.body;

    var productList = List<ProductModel>.from((jsonDecode(jsonData) as List).map((e) => ProductModel.fromMap(e)));

    for (final product in productList) {
      print("GOS;${product.id};${product.name};${product.search};${product.brand_id};${brandName};${product.micro_image_url};${product.thumb_image_url}".replaceAll("\n", "--"));
    }
  }

  static Future<void> loadShadesByProductId() async {
    final url = "https://findation.com/shades?product_id=";

    for (final id in productsIds) {
      final completeUrl = "$url$id/";
      final response = await http.get(Uri.parse(completeUrl));
      final jsonData = response.body;

      var shades = List<ShadeModel>.from((jsonDecode(jsonData) as List).map((e) => ShadeModel.fromMap(e)));
      for (final shade in shades) {
        print("GOS;${shade.id};${shade.product_id};${shade.name};${shade.search}".replaceAll("\n", "--"));
      }
    }
  }
}

